'use client';

import React, { useState } from 'react';
import { Document, Packer, Paragraph, TextRun } from 'docx';

interface MessageBubbleProps {
  message: string;
  isUser: boolean;
  timestamp: Date;
}

const downloadDocument = async (content: string, filename: string, format: 'txt' | 'docx' | 'csv') => {
  // Clean up the content for better formatting
  let cleanContent = content;
  
  // Add proper document header
  const header = `DORF NELSON & ZAUDERER
Legal Document
Generated: ${new Date().toLocaleDateString()}
===============================================

`;
  
  // Add disclaimer footer
  const footer = `

===============================================
DISCLAIMER: This is a template document generated by the Dorf Nelson & Zauderer Artificial Intelligence Assistant. This document is for informational purposes only and does not constitute legal advice or reliable legal research. Please review thoroughly for accuracy and consult with a qualified attorney before using this document for any purpose.

Â© ${new Date().getFullYear()} Dorf Nelson & Zauderer`;

  cleanContent = header + cleanContent + footer;
  
  let blob: Blob;
  
  switch (format) {
    case 'csv':
      // Convert to CSV format (simple conversion)
      const csvContent = cleanContent.split('\n').map(line => `"${line.replace(/"/g, '""')}"`).join('\n');
      blob = new Blob([csvContent], { type: 'text/csv' });
      break;
    case 'docx':
      try {
        // Create a proper Word document using docx library
        const doc = new Document({
          sections: [{
            properties: {},
            children: [
              new Paragraph({
                children: [
                  new TextRun({
                    text: "DORF NELSON & ZAUDERER",
                    bold: true,
                    size: 32,
                  }),
                ],
                alignment: 'center',
              }),
              new Paragraph({
                children: [
                  new TextRun({
                    text: `AI Generated: ${new Date().toLocaleDateString()}`,
                    size: 20,
                  }),
                ],
                alignment: 'center',
              }),
              new Paragraph({ text: "" }), // Empty line
              ...content.split('\n').map(line => 
                new Paragraph({
                  children: [
                    new TextRun({
                      text: line,
                      size: 22,
                    }),
                  ],
                })
              ),
              new Paragraph({ text: "" }), // Empty line
              new Paragraph({
                children: [
                  new TextRun({
                    text: "DISCLAIMER: This is a template document generated by the Dorf Nelson & Zauderer Artificial Intelligence Assistant. This document is for informational purposes only and does not constitute legal advice or reliable legal research. Please review thoroughly for accuracy and consult with a qualified attorney before using this document for any purpose.",
                    italics: true,
                    size: 18,
                  }),
                ],
              }),
              new Paragraph({
                children: [
                  new TextRun({
                    text: `Â© ${new Date().getFullYear()} Dorf Nelson & Zauderer`,
                    italics: true,
                    size: 18,
                  }),
                ],
                alignment: 'center',
              }),
            ],
          }],
        });

        const buffer = await Packer.toBuffer(doc);
        blob = new Blob([new Uint8Array(buffer)], { 
          type: 'application/vnd.openxmlformats-officedocument.wordprocessingml.document' 
        });
      } catch (error) {
        console.error('Error creating Word document:', error);
        // Fallback to text format
        blob = new Blob([cleanContent], { type: 'text/plain' });
      }
      break;
    default: // txt
      blob = new Blob([cleanContent], { type: 'text/plain' });
  }
  
  // Enhanced cross-browser download with multiple fallback methods
  try {
    // Method 1: Modern blob download (works on most browsers)
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    a.style.display = 'none';
    a.rel = 'noopener noreferrer';
    a.target = '_blank';
    document.body.appendChild(a);
    
    // Firefox and Safari compatibility: small delay and proper cleanup
    setTimeout(() => {
      a.click();
      setTimeout(() => {
        document.body.removeChild(a);
        URL.revokeObjectURL(url);
      }, 100);
    }, 0);
    
  } catch (error) {
    console.warn('Primary download method failed, trying fallback:', error);
    
    // Method 2: Fallback for older browsers or restrictive environments
    try {
      const reader = new FileReader();
      reader.onload = function() {
        const dataUrl = reader.result as string;
        const a = document.createElement('a');
        a.href = dataUrl;
        a.download = filename;
        a.style.display = 'none';
        document.body.appendChild(a);
        a.click();
        setTimeout(() => {
          document.body.removeChild(a);
        }, 100);
      };
      reader.readAsDataURL(blob);
      
    } catch (fallbackError) {
      console.error('All download methods failed:', fallbackError);
      
      // Method 3: Final fallback - open in new window with download suggestion
      const reader = new FileReader();
      reader.onload = function() {
        const dataUrl = reader.result as string;
        const newWindow = window.open('', '_blank');
        if (newWindow) {
          newWindow.document.write(`
            <html>
              <head><title>Download ${filename}</title></head>
              <body style="font-family: Arial, sans-serif; padding: 20px; text-align: center;">
                <h2>Download Ready</h2>
                <p>Right-click the link below and select "Save As" to download your file:</p>
                <a href="${dataUrl}" download="${filename}" style="font-size: 18px; color: #007bff;">${filename}</a>
                <br><br>
                <p style="color: #666; font-size: 14px;">If the download doesn't start automatically, use the right-click method above.</p>
              </body>
            </html>
          `);
          newWindow.document.close();
        } else {
          alert(`Download blocked by browser. Please allow pop-ups and try again, or contact support for the file: ${filename}`);
        }
      };
      reader.readAsDataURL(blob);
    }
  }
};

export default function MessageBubble({ message, isUser, timestamp }: MessageBubbleProps) {
  const [copied, setCopied] = useState(false);

  const formatTime = (date: Date) => {
    return date.toLocaleTimeString('en-US', { 
      hour: 'numeric', 
      minute: '2-digit',
      hour12: true 
    });
  };

  const handleCopyText = async () => {
    try {
      await navigator.clipboard.writeText(message);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000); // Reset after 2 seconds
    } catch (error) {
      console.error('Failed to copy text:', error);
      // Fallback for older browsers
      const textArea = document.createElement('textarea');
      textArea.value = message;
      document.body.appendChild(textArea);
      textArea.select();
      document.execCommand('copy');
      document.body.removeChild(textArea);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    }
  };

  // Check if message contains document content that can be downloaded
  const isDocument = message.length > 500 && (
    message.includes('CONTRACT') || 
    message.includes('AGREEMENT') || 
    message.includes('TEMPLATE') || 
    message.includes('DOCUMENT') ||
    message.includes('WHEREAS') ||
    message.includes('THEREFORE') ||
    message.includes('PARTY') ||
    message.includes('[') || // Placeholder fields
    message.includes('LEGAL') ||
    message.includes('TERMS') ||
    message.includes('CONDITIONS') ||
    message.includes('CLAUSE') ||
    message.includes('SECTION') ||
    message.includes('ARTICLE')
  );

  const handleDownload = async (format: 'txt' | 'docx' | 'csv') => {
    // Generate a descriptive filename based on content
    let docType = 'ai_response';
    
    // Check for legal document types
    if (message.includes('CONTRACT')) docType = 'contract';
    else if (message.includes('AGREEMENT')) docType = 'agreement';
    else if (message.includes('TEMPLATE')) docType = 'template';
    else if (message.includes('NDA') || message.includes('NON-DISCLOSURE')) docType = 'nda';
    else if (message.includes('EMPLOYMENT')) docType = 'employment_agreement';
    else if (message.includes('LEASE')) docType = 'lease_agreement';
    else if (message.includes('SERVICE')) docType = 'service_agreement';
    else if (message.includes('TERMS')) docType = 'terms_conditions';
    else if (message.includes('LEGAL')) docType = 'legal_analysis';
    else if (message.includes('RESEARCH')) docType = 'legal_research';
    else if (message.includes('MEMO')) docType = 'legal_memo';
    else if (message.includes('BRIEF')) docType = 'legal_brief';
    else if (message.includes('LETTER')) docType = 'legal_letter';
    else if (message.includes('OPINION')) docType = 'legal_opinion';
    else if (message.includes('ADVICE')) docType = 'legal_advice';
    else if (message.includes('GUIDANCE')) docType = 'legal_guidance';
    else if (message.includes('ANALYSIS')) docType = 'legal_analysis';
    else if (message.includes('REVIEW')) docType = 'legal_review';
    else if (message.includes('SUMMARY')) docType = 'legal_summary';
    else if (message.includes('REPORT')) docType = 'legal_report';
    
    const timestamp = new Date().toISOString().slice(0, 10); // YYYY-MM-DD
    const filename = `${docType}_${timestamp}.${format}`;
    
    await downloadDocument(message, filename, format);
  };

  return (
    <div className={`py-8 animate-slide-in-up ${isUser ? 'bg-gradient-to-r from-blue-50/40 to-indigo-50/30' : 'bg-white/60'} border-b border-gray-100/50 backdrop-blur-sm`}>
      <div className="max-w-4xl mx-auto px-4">
        <div className="flex items-start space-x-4">
          {/* Modern Avatar with Enhanced Design */}
          <div className={`w-11 h-11 rounded-2xl flex items-center justify-center flex-shrink-0 shadow-lg border-2 transition-all duration-300 hover:scale-110 ${
            isUser 
              ? 'bg-gradient-to-br from-indigo-600 via-blue-600 to-cyan-600 text-white border-blue-400/30 shadow-blue-500/30' 
              : 'bg-gradient-to-br from-slate-100 to-slate-200 text-slate-600 border-slate-200 shadow-slate-500/20'
          }`}>
            {isUser ? (
              <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
                <path fillRule="evenodd" d="M10 9a3 3 0 100-6 3 3 0 000 6zm-7 9a7 7 0 1114 0H3z" clipRule="evenodd" />
              </svg>
            ) : (
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 6.253v13m0-13C10.832 5.477 9.246 5 7.5 5S4.168 5.477 3 6.253v13C4.168 18.477 5.754 18 7.5 18s3.332.477 4.5 1.253m0-13C13.168 5.477 14.754 5 16.5 5c1.746 0 3.332.477 4.5 1.253v13C19.832 18.477 18.246 18 16.5 18c-1.746 0-3.332.477-4.5 1.253" />
              </svg>
            )}
          </div>
          
          {/* Message content - ChatGPT Style */}
          <div className="flex-1 min-w-0 relative group">
            {/* Modern Copy button for AI messages */}
            {!isUser && (
              <button
                onClick={handleCopyText}
                className="absolute -top-1 right-0 p-2.5 opacity-0 group-hover:opacity-100 transition-all duration-300 hover:bg-gradient-to-r hover:from-blue-50 hover:to-indigo-50 rounded-xl border border-transparent hover:border-blue-200 hover:shadow-md transform hover:scale-110"
                title="Copy message"
              >
                {copied ? (
                  <svg className="w-4 h-4 text-green-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2.5} d="M5 13l4 4L19 7" />
                  </svg>
                ) : (
                  <svg className="w-4 h-4 text-slate-500 hover:text-blue-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 16H6a2 2 0 01-2-2V6a2 2 0 012-2h8a2 2 0 012 2v2m-6 12h8a2 2 0 002-2v-8a2 2 0 00-2-2h-8a2 2 0 00-2 2v8a2 2 0 002 2z" />
                  </svg>
                )}
              </button>
            )}
            
            <div className="prose prose-sm max-w-none">
              <p className="text-gray-900 leading-relaxed whitespace-pre-wrap m-0">{message}</p>
            </div>
            
            {/* File attachment indicator */}
            {isUser && message.includes('ðŸ“Ž Uploaded files:') && (
              <div className="mt-3 p-3 bg-blue-50 border border-blue-200 rounded-lg">
                <div className="flex items-center space-x-2">
                  <svg className="w-4 h-4 text-blue-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15.172 7l-6.586 6.586a2 2 0 102.828 2.828l6.414-6.586a4 4 0 00-5.656-5.656l-6.415 6.585a6 6 0 108.486 8.486L20.5 13" />
                  </svg>
                  <span className="text-sm text-blue-800 font-medium">Files uploaded and analyzed</span>
                </div>
              </div>
            )}
            
            {/* Modern Download Section - Always show for AI responses */}
            {!isUser && (
              <div className="mt-6 p-5 bg-gradient-to-br from-slate-50 via-blue-50/30 to-indigo-50/30 rounded-2xl border-2 border-slate-200/50 shadow-lg hover:shadow-xl transition-all duration-300">
                <div className="flex items-center mb-4">
                  <div className="w-8 h-8 bg-gradient-to-br from-blue-500 to-indigo-600 rounded-lg flex items-center justify-center mr-3 shadow-md">
                    <svg className="w-4 h-4 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2.5} d="M12 10v6m0 0l-3-3m3 3l3-3m2 8H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
                    </svg>
                  </div>
                  <span className="text-sm font-bold text-gray-800">Export Document</span>
                </div>
                <div className="flex flex-wrap gap-3">
                  <button
                    onClick={() => handleDownload('docx')}
                    className="group relative flex items-center space-x-3 px-5 py-3.5 bg-gradient-to-r from-blue-500 to-blue-600 hover:from-blue-600 hover:to-blue-700 rounded-xl transition-all duration-300 text-sm border-2 border-blue-400/30 shadow-lg shadow-blue-500/30 hover:shadow-xl hover:shadow-blue-500/40 transform hover:scale-105 active:scale-95 overflow-hidden"
                  >
                    <div className="absolute inset-0 bg-white/20 opacity-0 group-hover:opacity-100 transition-opacity"></div>
                    <div className="w-7 h-7 bg-white/20 backdrop-blur-sm rounded-lg flex items-center justify-center relative z-10">
                      <svg className="w-4 h-4 text-white" viewBox="0 0 24 24" fill="currentColor">
                        <path d="M12.5 2L3 4.5v15L12.5 22l9.5-2.5v-15L12.5 2zM8.5 7.5h2v9h-2v-9zm4 0h2v9h-2v-9z"/>
                      </svg>
                    </div>
                    <span className="font-bold text-white relative z-10">Download as Word</span>
                  </button>
                  <button
                    onClick={() => handleDownload('csv')}
                    className="group relative flex items-center space-x-3 px-5 py-3.5 bg-gradient-to-r from-emerald-500 to-green-600 hover:from-emerald-600 hover:to-green-700 rounded-xl transition-all duration-300 text-sm border-2 border-emerald-400/30 shadow-lg shadow-emerald-500/30 hover:shadow-xl hover:shadow-emerald-500/40 transform hover:scale-105 active:scale-95 overflow-hidden"
                  >
                    <div className="absolute inset-0 bg-white/20 opacity-0 group-hover:opacity-100 transition-opacity"></div>
                    <div className="w-7 h-7 bg-white/20 backdrop-blur-sm rounded-lg flex items-center justify-center relative z-10">
                      <svg className="w-4 h-4 text-white" viewBox="0 0 24 24" fill="currentColor">
                        <path d="M3 3h18v18H3V3zm3 3v12h12V6H6zm2 2h8v2H8V8zm0 3h8v2H8v-2zm0 3h8v2H8v-2z"/>
                      </svg>
                    </div>
                    <span className="font-bold text-white relative z-10">Download as Excel</span>
                  </button>
                </div>
              </div>
            )}

            {/* Modern Timestamp */}
            <div className="mt-3 flex items-center space-x-2 text-xs text-slate-500">
              <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z" />
              </svg>
              <span className="font-medium">{formatTime(timestamp)}</span>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}